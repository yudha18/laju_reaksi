                        <center><p class="text_content">Penerapan Konsep Laju Reaksi</p></center>
                        <br>
                        <p class="text_content3">Konsep laju reaksi dapat diterapkan dalam kehidupan sehari-hari maupun industri.
                            Penerapan konsep tersebut berkaitan dengan factor-faktor yang mempengaruhi laju reaksi.
                            Dengan mengetahui factor-faktor yang mempengaruhi lajunya, suatu reaksi dapat dikontrol.
                            Hal inilah yang melandasi penerapan konsep laju reaksi baik dalam kehidupan ataupun dalam industri. <br>
                        </p>
                        <p class="text_content2">Contoh penerapan konsep laju reaksi di bidang industri: <br>
                                1.	pembuatan gas ammonia dengan proses Haber Bosch<br>
                                pada proses Haber Bosch digunakan Katalis serbuk besi (Fe).
                                Katalis Fe berfungsi mempercepat reaksi melalui aksi.<br>
                                2.	Pembuatan asam nitrat <br>
                                Metode yang sering digunakan dalam pembuatan asam nitrat adalah proses Ostwald. <br>
                                Asam nitrat dibuat dari ammonia dan oksigen dengancara dipanaskan pada suhu sekitar 800&degC dengan dengan katalis platinum-rodium.<br>
                                Contoh penerapan konsep laju reaksi di bidang industry: <br>
                                banyak penggunaan zat-zat dalam kehidupan sehari-hari dalam bentuk serbuk atau butiran halus seperti sabun cuci garam, dan susu bubuk.
                                Hal ini bertujuan agar reaksi berlagsung cepat dengan prinsip memperluas permukaan bidang sentuh. <br>
